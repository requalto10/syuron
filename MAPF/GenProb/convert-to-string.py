def convert_grid_to_string(grid):
    return [''.join('.' if cell == 0 else '@' for cell in row) for row in grid]

def write_to_file(filename, problems):
    with open(filename, 'w') as file:
        for problem_number, (grid, agents, start_points, goal_points, time_step) in enumerate(problems, start=1):
            file.write(f"# Problem {problem_number}:\n")
            string_grid = convert_grid_to_string(grid)
            for line in string_grid:
                file.write(line + '\n')
            file.write(f"Agents = {agents}\n")
            file.write(f"Start_Points = {start_points}\n")
            file.write(f"Goal_Points = {goal_points}\n")
            file.write(f"Time_Step = {time_step}\n\n")

